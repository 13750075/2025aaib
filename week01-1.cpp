#include <stdio.h>///檔名不能寫錯
///stdio的意思是標準的standard input/output
///有了stdio.h的外掛,才能用scanf() prinf()
int main()
{
    int a,b;///整數a,b
    scanf("%d",&a);///正課剛剛交的,掃描讀東西
    scanf("%d",&b);///可以用複製的,再改b
    printf("%d",a+b);///實習課有交,印東西

}
